# Теперь для подгонки функции sin. Все параметры должны быть установлены 
# близко к ожидаемому значению чтобы это работало хорошо, именно поэтому
# сначала код оценивает параметры.
def fit_sin(sol):
    # Оценить параметры amp, M, theta, потому что нужно начать близко к 
    # ожидаемому значению, чтобы получить правильную подгонку.
    amp, TT, rt_st = find_fit_params(sol)

    # Извлечь значения x и psi из решения.
    xdata = sol['t']
    ydata = np.real(sol['y'][0])

    # Здесь случай ошибки. Чтобы просто отмечать случай как неразрешимый, если
    # недостаточно данных для моего простого алгоритма. Для сетки периодов 
    # медленных колебаний эти данные не требуются, поскольку у нас есть больше 
    # данных.

    if rt_st is None:
        return None
    
    MM = 2 * pi / TT # Переключение с периода на волновое число.
    theta = - MM * xdata[rt_st] # Изменить масштаб x_0 на угловой сдвиг.

    # Предотвратить слишком большое отклонение алгоритма подгонки функции от
    # ожидаемых значений.
    amp_tol = 0.1
    M_tol = 0.1
    th_tol = 0.1 * pi

    # Минимальное, максимальное и начальное значения для алгоритма
    # подгонки функции в правильной форме.
    min_arr = np.array([(1.0 - amp_tol) * amp, MM - M_tol * MM , theta - th_tol])
    max_arr = np.array([(1.0 + 2 * amp_tol) * amp, MM + M_tol * MM, theta + th_tol])
    start_arr = np.array([amp, MM, theta])

    # И позвони SciPy.
    popt, pcov = curve_fit(sin_fit, xdata, ydata, p0=start_arr, bounds=(min_arr, max_arr))
    return popt


