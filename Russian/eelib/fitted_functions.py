# Оценка амплитуды
# Получив решение, оценить амплитуду как наименьшее расстояние в решении от 0.
# Фактическая амплитуда должна быть немного больше этого значения, но ее можно
# найти с помощью функции подбора. Это просто дает первоначальное предположение.
def find_amp(sol):
    arr = np.real(sol['y_events'][1][:, 0])
    return np.max(np.abs(arr))

# Это возвращает расстояние между нулями в количестве точек.
# Чтобы найти точку, нужно умножить на расстояние t между точками.
def find_root_dif(y_points):
    root_list = find_root_points(y_points)
    y_diff = np.array(root_list[1:])- np.array(root_list[:-1])
    return np.average(y_diff)

# Используется для оценки значений, для которых кривая пересекает ось.
# Используется как для оценки x_0, так и для оценки периода.
# y_points = y1_list_0 = np.real(sol['y'][0])
# Учитывая точки y, найти индексы нулей.
def find_root_points(y_points):
    y_mult = np.array(y_points[:-1])*np.array(y_points[1:])
    zero_index = np.nonzero(y_mult == 0)
    root_index = np.nonzero(y_mult < 0)
    full_index = list(root_index[0]) + list(zero_index[0])
    full_index.sort()
    return full_index

# Оценка x_0
# Здесь я хочу найти первые два значения x, где функция меняет знак и 
# вернуть то, где знак меняется с отрицательного на положительный, 
# поскольку функция sin начинается с 0 и увеличивается с этого момента.
def find_root_start(sol):
    y1_list = np.real(sol['y'][0])
    root_ls = find_root_points(y1_list)
    if len(root_ls) < 2:
        rt_start = None
    elif y1_list[root_ls[0]+1] > 0: 
        rt_start = root_ls[0]
    else: 
        rt_start = root_ls[1]
    return rt_start

# Оценка периода
# Функция дважды проходит через 0 за период. Для функции sin она начинается
# с нуля, проходит через ноль один раз, а затем заканчивается на нуле. Я могу
# использовать это для оценки периода как удвоенное расстояние между корнями.
def find_period(sol):
    t_list = sol['t']
    y1_list = np.real(sol['y'][0])
    root_list = find_root_points(y1_list)
    t_root_list = []
    for i in root_list: 
        t_root_list.append(t_list[i])
    if len(t_root_list) == 0:
        return None
    t_diff = np.array(t_root_list[1:])- np.array(t_root_list[:-1])
    t_dif_ave = np.average(t_diff)
    return t_dif_ave * 2


# Теперь нужно вызвать дескриптор функции и вернуть результат всех трех оценок.
def find_fit_params(sol):
    amp = find_amp(sol)
    rt_start = find_root_start(sol)
    MM = find_period(sol)
    return amp, MM, rt_start       
