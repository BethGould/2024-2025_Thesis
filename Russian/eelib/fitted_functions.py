# 1: Основы тригонометрии
#    Подбор дескрипторов функций для тригонометрических функций.
# Параметрами являются amp (амплитуда), k (волновое число), 
# и theta (угол при x = 0) или x0 (координата x для начала функции синус).

def sin_fit(x, amp, k, theta):
    return amp * np.sin(k * x + theta)

def sin_fit_2(x, amp, k, x0):
    return amp * np.sin(k * (x - x0))

# 2: Эллиптическая функция Якоби (не используется)
#    Дескрипторы функций подгонки для эллиптических функций.
#    x0 для cn должно привести функцию к максимуму, в то время как sn начинается
#    с нуля, как синус.
# Параметрами являются k (умножается x, как волновое число), 
#                      x0 (координата x для начала функции),
#                      m (эллиптический параметр Якоби),
#                      A (амплитуда колебаний), 
#                      и y0 (координата оси y для колебаний).

def cn_fit(x, k, x0, m, A, y0):
    cn,sn,dn,ph = ellipj(k * (x - x0), m)
    return A * cn - y0

def sn_fit(x, k, x0, m, A, y0):
    cn,sn,dn,ph = ellipj(k * (x - x0), m)
    return A * sn - y0

def dn_fit(x, k, x0, m, A, y0):
    cn,sn,dn,ph = ellipj(k * (x - x0), m)
    return A * dn - y0

def cn2_fit(x, k, x0, m, A, y0):
    cn,sn,dn,ph = ellipj(k * (x - x0), m)
    return A * (cn)**2 - y0

def sn2_fit(x, k, x0, m, A, y0):
    cn,sn,dn,ph = ellipj(k * (x - x0), m)
    return A * (sn)**2 - y0

# 3: Повернутая секанс / косеканс в квадрате (неиспользуемая и минимально 
#    протестированная)
#    Функция подгонки для кривой с повернутым секансом в квадрате.
#    Период известен, поэтому его нужно будет задать с помощью лямбда-функции.
#    > fun = lambda x, amp, tilt, x0: skew_csc_sq(x, period, amp, tilt, x0)
# Параметрами являются period (период колебаний / повторений), 
#                      amp (минимальное значение секанса в квадрате, на которое
#                           умножаются все значения секанса в квадрате),
#                      tilt (угол поворота кривой),
#                      и x0 (ось x, минимум кривой секанса в квадрате).

# Учитывая x1 и угол поворота theta (в радианах), найти значения x2 и y2,
# полученные при повороте этой точки на кривой tan^2 вокруг начала координат
# по углу theta.
def rotation_tan_sq(x1, theta):
    x2 = np.cos(theta)* x1 + np.sin(theta)*np.power(np.tan(x1),2)
    y2 = - np.sin(theta)* x1 + np.cos(theta)*np.power(np.tan(x1),2)
    return x2, y2



# Определяется минимальное значение x, полученное при повороте стандартной
# кривой tan^2 вокруг начала координат по углу наклона (в радианах).
def find_min_x_skew_tan(tilt):

    # Нули производной - это экстремальные точки, в данном случае минимумы. 
    # Это производная d/dx1 от:
    #        x2 = np.cos(theta)* x1 + np.sin(theta)*np.power(np.tan(x1),2)
    fun = lambda x: np.cos(tilt) + 2 * np.sin(tilt)* np.tan(x)*np.power(np.cos(x),-2)

    # Избегайте сингулярности на границах периодов.
    eps = 0.000001 

    # И возвращает результаты поиска этого нуля.
    ret = brentq(fun, -pi / 2 + eps, 0)
    return ret

# Период csc^2 равен pi. 

# Учитывая точку x2 (координату x повернутой функции tan^2), найти значение
# функции y2 (значение повернутой функции tan^2).
# Кривая tan^2 поворачивается по углу tilt (в радианах), и точка x1 
# (координату x точки кривая без поворота tan^2) находится между a и b.
def find_skew_tan(x2, a, b, tilt):
    # x2 > x1
    #a = min_x1 (or 0)
    #b = x2 (or 0)

    # Определять функцию, для которой нужно найти нули.
    # x2 = np.cos(theta)* x1 + np.sin(theta)*np.power(np.tan(x1),2)
    fun = lambda x: x2 - np.cos(tilt)* x - np.sin(tilt)*np.power(np.tan(x),2)

    # Найти значение x1, которое дает значение x2, когда функция tan^2 
    # поворачивается по углу tilt.
    # Верхняя граница равна либо 0, либо желаемому x2.
    # Нижняя граница равна либо 0, либо минимально возможному значению x1 для
    # повернутой кривой.
    x1 = brentq(fun, a, b)

    # x2b здесь теперь должен возвращать введенный x2, поэтому нужен только y2
    x2b, y2 = rotation_tan_sq(x1, tilt)

    return y2

# Функция подгонки для повернутого секанса в квадрате.
# Параметрами являются period (период колебаний / повторений), 
#                      amp (минимальное значение секанса в квадрате, на которое
#                           умножаются все значения секанса в квадрате),
#                      tilt (угол поворота кривой),
#                      и x0 (ось x, минимум кривой секанса в квадрате).
def skew_csc_sq(x, period, amp, tilt, x0):
    x_new = x - x0 # Сдвинуть x на x0 (координату x0 минимума).
    x_rescale = x_new / period # Разделить на период.
    period_count = x_rescale // 1.0 
                    # Количество периодов от центра не используется.
    x_per_rem = x_rescale % 1.0 # Дробная часть задает функциональное значение.
    x_rem = x_per_rem * pi # Координата от минимума слева от x
    x_neg_rem = (x_per_rem-1) * pi # Координата от минимума справа от x

    # Точка минимума x правого периода определяет, какую кривую анализировать, 
    # чтобы найти значение. Если кривая справа имеет это значение, использовать
    # это значение, в противном случае использовать левую кривую.
    # Корень производной dx2/dx1 возвращает координату x (x1) минимума.
    # Затем нужно повернуть эту функцию, чтобы найти координаты x и y 
    # повернутой функции.
    min_x1 = find_min_x_skew_tan(tilt)
    min_x2, min_y2 = rotation_tan_sq(min_x1, tilt)

    # Теперь выберите, какую кривую использовать. 
    if x_neg_rem == min_x2:
        y = min_y2 # Если значение находится на границе, значение y уже известно.
    elif x_neg_rem > min_x2:
        # Если значение находится на правой кривой, найти значение на этой кривой.
        # Знаем, что x1 отрицательно и больше минимума.
        y = find_skew_tan(x_neg_rem, min_x1, 0, tilt)
    else:
        # Если значение на правой кривой отсутствует, найти значение на левой
        # кривой.
        # Знаем, что x1 положительно и меньше x2.
        y = find_skew_tan(x_rem, 0, x_rem, tilt)

    # Изменить масштаб функции на амплитуду и добавить амплитуду, чтобы 
    # переключиться с tan^2 на sec^2, поскольку sec^2 = tan^2 + 1.
    y = y * amp + amp
    return y

